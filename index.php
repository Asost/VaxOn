        <h1>FAQ about COVID-19 and the vaccine</h1>
        <h3>1. Why are vaccines so important in our daily lives?</h3>
        <p>Vaccines are one of the best tools we have to prevent disease and death in both children and adults. Getting vaccinated is one of the easiest things that we can do to keep ourselves and our children healthy. When I think about all the things I do to stay healthy—eat well, exercise, manage stress—that’s a lot of work. But it’s easy for me to go and get my flu shot, or bring my kids in to get their vaccines. And when I do that, I’m preventing disease, not just in myself and my family, but also in the communities where I live and work.</p>
        <h3>2. How safe is the COVID-19 vaccine?</h3>
        <p>Millions of people have safely received a COVID-19 vaccine Over 52 million doses of COVID-19 vaccine were administered in the United States from December 14, 2020, through February 14, 2021. COVID-19 vaccines are safe and effective.</p>
        <h3>3. Can I get vaccinated against COVID-19 while I am currently sick with COVID-19?</h3>
        <p>No. People with COVID-19 who have symptoms should wait to be vaccinated until they have recovered from their illness and have met the criteria for discontinuing isolation; those without symptoms should also wait until they meet the criteria before getting vaccinated. This guidance also applies to people who get COVID-19 before getting their second dose of vaccine.</p>
        <h3>4. Who is most at risk during the COVID-19 pandemic?</h3>
        <p>Older adults and people of any age who have serious underlying medical conditions like heart or lung disease or diabetes seem to be at higher risk for developing more serious complications from COVID-19 illness.</p>
